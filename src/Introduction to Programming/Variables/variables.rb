# Variables - placeholder to store a value

location = "Georgia"
puts location # Georgia

my_num = 40
my_num + 10 # we're not storing this value anywhere!
puts my_num # 40

# To modify a variable's value, we need to assign it to variable again
my_num += 10 # my_num = my_num + 10
puts my_num # 50

# Variables & comparison
is_even = my_num % 2 == 0
puts is_even # true
