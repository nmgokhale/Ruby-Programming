# Boolean Data Type
# operations
# ! (not)
# && (and)
# || (or)

puts true # true
puts false # false
puts !true # false
puts !false # true

puts false && false # false
puts false && true # false
puts true && false # false
puts true && true # true

puts false || false # false
puts false || true # true
puts true || false # true
puts true || true # true
